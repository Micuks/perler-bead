def hex_to_rgb(h):
    return (int(h[1:3], 16), int(h[3:5], 16), int(h[5:7], 16))


# Filter out transparent/special colors (same logic as gnim.fun)
def _is_usable(entry):
    mard = entry[2]
    if mard == 'H1':  # pearl white
        return False
    if mard[0] in ('P', 'Q', 'R', 'T', 'Y') or mard.startswith('ZG'):
        return False
    return True


